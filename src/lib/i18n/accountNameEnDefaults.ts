/**
 * يبحث عن ترجمة إنجليزية افتراضية لاسم عربي.
 * مطابقة دقيقة فقط — لا prefix/suffix لتفادي إنتاج هجين مثل "Assets الثابتة"
 * لو الاسم العربي مركّب ولم يكن في القاموس بكامله. لو لا توجد مطابقة دقيقة
 * يُرجع undefined ويتمّ الرجوع للاسم العربي كما هو.
 */
export function defaultEnglishName(
  nameAr: string | null | undefined,
  map: Readonly<Record<string, string>>,
): string | undefined {
  const key = (nameAr ?? '').trim();
  if (!key) return undefined;
  return map[key];
}
